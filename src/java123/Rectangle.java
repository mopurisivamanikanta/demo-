package java123;

class Rectangle{ 
   private int length; 
   private int breadth; 
  
public Rectangle(int breadth, int length){
	  this.breadth = breadth;
	  this.length = length;
	 } 

public int getBreadth() { 
 return breadth; 
} 

public int getLength(){
	return length;
}
public void setLength(int length) { 
 this.length = length; 
} 
public void setBreadth(int breadth) {
	this.breadth = breadth;
}
}
